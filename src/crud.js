/* Descrição da Atividade
Nesta atividade, você deverá desenvolver um programa em JavaScript responsável por realizar um CRUD (Create, Read, Update e Delete) de veículos.

Todas as interações com o usuário devem ser realizadas exclusivamente através de prompt, e as informações devem ser exibidas utilizando console.log e/ou alert, conforme necessário.
 */

/* Requisitos Gerais
Cada veículo deve possuir os seguintes dados:

Identificador (ID)
Modelo
Marca
Ano
Cor
Preço

Os veículos devem ser armazenados em uma lista (array).

Cada veículo deve possuir um identificador único, gerado automaticamente pelo sistema.

A lista de veículos deve estar ordenada pelo preço.

A funcionalidade de filtro deve ser realizada por marca e apresentar os veículos ordenados pelo preço.

Apenas os campos cor e preço podem ser atualizados.

O sistema deve sempre retornar ao menu inicial após a execução de uma ação. */
